-- Benefits of Views

-- 1. Simplify the queries.
-- 2. Reduce the impact of changes of your database design.
-- 3. (Abstraction Layer) Views can act as an Interface that anyone can write his/her query against.
-- in a way that will protect the namings being used, and also allow changes to the underlying tables.
-- 4. Restrict access to the underlying table, by filtering records or omit few columns that you don't want the user to be aware of!
