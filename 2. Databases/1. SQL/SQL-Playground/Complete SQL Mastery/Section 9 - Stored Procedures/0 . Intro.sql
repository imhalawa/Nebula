/*
- Stored Procedures
- Stored Procedures a subroutine available to applications that access a relational database management system
- As an old practice, we don't mix our Application code with SQL code for few reasons,
	1. It will become messy
	2. It's harder to debug
	3. Every change will require compilation (for compiled langs)
- That's why we store our sql queries into Stored Procedures or a function


- Stored procedures are database objects that contains a block of SQL code.
- On the application side, it's being used to get or save the data.
- Store & Organize our SQL code.

- Also it has other benefits, that some Database engines performs optimization for stored procedures
  that it could be executed faster

- Allows us to enforce data security



*/