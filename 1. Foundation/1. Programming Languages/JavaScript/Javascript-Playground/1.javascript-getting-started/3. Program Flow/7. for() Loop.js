/**
 * for(initializatin;condition;step){
 *      // Do Something
 * }
 *
 *  initialization: executes one-time only, to define the iterator.
 *  condition: determines whether to continue or stop (termination condition)
 *  step: manipulating the iterator in a certain way e.g. increase by 1  
 */



for (let i = 0; i < 3; i++) {
    console.log(i);
}


// 0 1 2


